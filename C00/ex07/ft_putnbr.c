void	signe(int nb)
{
	if (nb < 0)
	{
		write(1, &"-", 1);
	}
}

void	convert(char nk)
{
	write(1, &nk, 1);
}

void	ft_putnbr(int nb)
{
	signe(nb);
	if (nb < 0)
	{
		nb = (-1) * nb;
	}
	if (nb > 10)
	{
		ft_putnbr(nb / 10 );
	}
	nb = nb % 10 + 48;
	convert(nb);
}
