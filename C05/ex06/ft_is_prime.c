int	ft_is_prime(int	nb)
{
	unsigned int	c;

	if (nb < 3 || !(nb % 2))
		return (nb == 2);
	c = 3;
	while (c * c <= (unsigned int)nb)
	{
		if (!(nb % c))
			return (0);
		c += 2;
	}
	return (1);
}
