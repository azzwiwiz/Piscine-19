int	ft_strcmp(char	*s1, char	*s2);

int	ft_strcmp(char	*s1, char	*s2)
{
	int	c;
	int	i;

	i = 0;
	c = 0;
	while (s1[c] <= '\0' )
	{
		c++;
	}
	while (s2[i] <= '\0')
	{
		i++;
	}
	return (c - i);
}
